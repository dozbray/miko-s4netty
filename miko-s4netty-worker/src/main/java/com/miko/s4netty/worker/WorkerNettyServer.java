/**
 * @author miroslavkopecky
 */

@Component
public class WorkerNettyServer {

    @Autowired
    @Qualifier("serverBootstrap")
    private ServerBootstrap serverBootstrap;

    @Autowired
    @Qualifier("tcpSocketAddress")
    private InetSocketAddress tcpPort;

    private final Logger logger = LoggerFactory.getLogger(WorkerNettyServer.class);

    private Channel workerChannel;

    public void setServerBootstrap(ServerBootstrap serverBootstrap){
        this.serverBootstrap = serverBootstrap;
    }

    @PostConstruct
    public void start() throws Exception{
        logger.debug("Starting Worker port= " + tcpPort);
        workerChannel = serverBootstrap.bind(tcpPort).channel();
    }

    @PreDestroy
    public void stop(){
        workerChannel.close();
    }


}
